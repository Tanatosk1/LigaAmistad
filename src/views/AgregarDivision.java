    private void btnAgregarDivisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDivisionActionPerformed
            String division= txtAgregarDivision.getText();
            if (txtAgregarDivision.getText().equals("")){
                ImageIcon icon = new ImageIcon(getClass().getResource("/resources/warning.png"));
                JOptionPane.showMessageDialog(rootPane, "Debe escribir el nombre de una divisi贸n","Nombre de la divisi贸n", JOptionPane.QUESTION_MESSAGE, icon);
            }else{
                try {
                    gd.guardarDivision(division);
                } catch (SQLException ex) {
                    Logger.getLogger(AgregarDivision.class.getName()).log(Level.SEVERE, null, ex);
                }
                dispose();
            }
        
    }//GEN-LAST:event_btnAgregarDivisionActionPerformed

