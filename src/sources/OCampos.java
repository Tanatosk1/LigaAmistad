public class OCampos {
    private int dia;
    private int hora;
    
    public OCampos(int dia, int hora){
        this.dia = dia;
        this.hora = hora;
    }
    
    public int getDia(){
        return this.dia;
    }
    
    public int getHora(){
        return this.hora;
    }
}
